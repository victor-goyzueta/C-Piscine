#include <unistd.h>

void	ft_print_comb(void)
{
	char	u;
	char	d;
	char	c;

	u = '0';
	while (u <= '7')
	{
		d = u + 1;
		while (d <= '8')
		{
			c = d + 1;
			while (c <= '9')
			{
				write(1, &u, 1);
				write(1, &d, 1);
				write(1, &c, 1);
				if (!(u == '7' && d == '8' && c == '9'))
					write(1, ", ", 1);
				c++;
			}
			d++;
		}
		u++;
	}
}
